
# in , not in, are the membership operators.
# it checks the value or element is exists in the list or not.

ename="pavankalyan"

size = [1, 2, 3, 4, 5]

print("ka" in ename)  # True
print("kan" in ename)  # False
print("kay" not in ename)  # True

print( 2 in size)  # True
print( 8 in size)  # False
print( 9 not in size) # True