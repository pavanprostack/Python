
# Lambda
# if you want to create function for instance use lambda function is best.

# Syntax : lambda parameters : expressions

# Named function
# 1.
def add(n):
    return n+1

print(add(10))

# 2.
def sum(a,b):
    return a+b

print(sum(10,20))


# Lambda Function
# 1.
add=lambda n : n+1

print(add(20))

# 2.
sum=lambda a, b : a+b

print(sum(50,60))
