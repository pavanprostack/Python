
# data structures

# 1. list
# group of values as one entity. where duplicates are allowed. it is mutable.
list=[10, 20, 30, 40]

# 2. tuple
# group of values as one entity. where duplicates are not allowed. it is immutable.
tuple=(10, 20, 30, 40)

# 3. set
# group of values as one entity. where duplicates are not allowed. it is mutable.
set={10, 20, 30, 40}

# 4. dict
# group of key-value pairs as one entity. where duplicates are not allowed. it is mutable.
dict={'id':101, 'name':"pavan"}

print(list)
print(tuple)
print(set)
print(dict)