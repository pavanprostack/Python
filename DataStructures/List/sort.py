
# sort() is by by default gives Ascending order.

enames = ["Rahul","Sonia","Priyanka","Modi"]
eids = [4,101,105,102,3,99,5]

enames.sort()
eids.sort()

print(enames)
print(eids)
      
      
# For Descending order.
      
enames.sort(reverse=True)
eids.sort(reverse=True)

print(enames)
print(eids)